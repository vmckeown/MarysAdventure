export function isColliding(x, y, playerSize = 32, npcs = [], objects = []) {
    const half = playerSize / 2;

    // tile collision
    const tileX = Math.floor(x / TILE_SIZE);
    const tileY = Math.floor(y / TILE_SIZE);
    if (tileY >= 0 && tileY < world.length && tileX >= 0 && tileX < world[tileY].length) {
        const tile = world[tileY][tileX];
        if (SOLID_TILES.includes(tile)) return true;
    }

    // NPC collision
    for (const npc of npcs) {
        if (!npc) continue;
        if (Math.abs(npc.x - x) < (npc.size + playerSize) / 2 &&
            Math.abs(npc.y - y) < (npc.size + playerSize) / 2) {
            return true;
        }
    }

    // Object collision
    for (const obj of objects) {
        if (!obj) continue;
        if (Math.abs(obj.x - x) < (obj.size + playerSize) / 2 &&
            Math.abs(obj.y - y) < (obj.size + playerSize) / 2) {
            return true;
        }
    }

    return false;
}



// ===== Draw Layers =====
export function drawWorld(ctx, camera) {
    const startCol = Math.floor(camera.x / TILE_SIZE);
    const endCol = Math.ceil((camera.x + camera.width) / TILE_SIZE);
    const startRow = Math.floor(camera.y / TILE_SIZE);
    const endRow = Math.ceil((camera.y + camera.height) / TILE_SIZE);

    for (let y = startRow; y < endRow; y++) {
        for (let x = startCol; x < endCol; x++) {
            if (world[y] && world[y][x] !== undefined) {
                const tileType = world[y][x];
                ctx.fillStyle = TILE_COLORS[tileType];
                ctx.fillRect(x * TILE_SIZE, y * TILE_SIZE, TILE_SIZE, TILE_SIZE);
            }
        }
    }
}


export function drawObjects(ctx, camera, objects) {
    for (const obj of objects) {
        ctx.fillStyle = obj.color;
        ctx.fillRect(obj.x, obj.y, obj.width, obj.height);
    }
}

// ===== Helpers =====
export function getTile(x, y) {
    if (x < 0 || y < 0 || y >= WORLD_ROWS || x >= WORLD_COLS) return 3; // treat out of bounds as solid
    return world[y][x];
}


export function rectsOverlap(x1, y1, w1, h1, x2, y2, w2, h2) {
    return !(x1 + w1 < x2 || x1 > x2 + w2 || y1 + h1 < y2 || y1 > y2 + h2);
}

export function worldToTile(x, y) {
  return {
    x: Math.floor(x / TILE_SIZE),
    y: Math.floor(y / TILE_SIZE)
  };
}

export function tileToWorld(tileX, tileY) {
  return {
    x: tileX * TILE_SIZE + TILE_SIZE / 2,
    y: tileY * TILE_SIZE + TILE_SIZE / 2
  };
}




